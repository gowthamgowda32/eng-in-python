#lists

my_list = [1, 2, 3, 4, 5]
print(my_list)
my_list.append(6) #adding element to the list
print(my_list)
my_list.remove(3) #removing element from the list
print(my_list)
my_list.insert(2,10)
print(my_list)
my_list.pop() #removing last element from the list
print(my_list)
my_list.pop(1) #removing element at index 1
print(my_list)

#slocing the list
print(my_list[1:5])
print(my_list[2:])
print(my_list[1:])
print(my_list[::2]) #every second element
print(my_list[::-1]) #reversing the list

